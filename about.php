            <h1>“I read; I travel; I become” ― Derek Walcott</h1>

            <p> Izifiso is not only a company, but a journey, the journey that started in March, 2017. Keeping in mind the meaning of the word in original Zulu language, Izifiso had come up with its exciting tagline: "Where desires get wings."</p>

<p> The most beautiful things in life are perhaps moments-accentuated with wilderness and the desires of the heart. Nothing can surpass the beauty of a sunset dipping in the melancholic melody of country music. The beauty that surrounds us - the nature that surrounds us just needs to be explored and harnessed, to turn it to a lifetime experience and we at Izifiso give wings to these unharnessed desires of the heart.</p>

<p> Mousuni Backpackers’ Camp was Izifiso’s first venture, which was set up in November, 2017. From then, the number got expanded to seven backpackers’ camps at different parts of West Bengal and Izifiso is eyeing something more.</p>

<p>But our focus is just not to set up camps or organize treks, but to think differently about travel. So, come with us, travel away from home, feel the goose bumps break into the skin in the eerie night, sweat under the scorching sun on an uphill trek, dance in the twilight sands to the waves that crash on the shore, whisper to the mangroves, gape at the constellations and let the mountain winds lull you to sleep. Let all these be a part of your story and your life and let them turn you into a story teller. We at Izifiso, aim at offering such off-beat travel experiences by making travel a venture to the dreamland.</p>

<p>Explore, educate and evolve.</p>

